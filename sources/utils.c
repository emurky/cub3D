#include "../includes/cub3d.h"

int	is_player_dir(char c)
{
	return (c == 'N' || c == 'S' || c == 'E' || c == 'W');
}

int	create_rgb(int r, int g, int b)
{
	return (r << 16 | g << 8 | b);
}
